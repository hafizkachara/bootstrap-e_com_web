function Myfunc_cat() {
    let category_btn = document.getElementById("btn-toggle_cate");
    let season_btn = document.getElementById("btn-toggle_blous");
    let size_btn = document.getElementById("btn-toggle_size");
    let color_btn = document.getElementById("btn-toggle_color");
    let brand_btn = document.getElementById("btn-toggle_brand");
    let price_btn = document.getElementById("btn-toggle_price");

    if (category_btn.style.display == "flex") {
        category_btn.style.display = "none"
    } else {
        category_btn.style.display = "flex";
        season_btn.style.display = "nonne";
        size_btn.style.display = "none";
        color_btn.style.display = "nonne";
        brand_btn.style.display = "none";
        price_btn.style.display = "none";
    }
}
